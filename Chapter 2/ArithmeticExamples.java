public class ArithmeticExamples {
    public static void main(String[] args) {
        int a = 2;
        int b = 3;
        int c = 5;
        double average = (a + b + c) / 3.0;
        System.out.println(average);
        double r = 1.3;
        double volume = (4 * Math.PI * Math.pow(r, 3)) / 3;
        System.out.println(volume);
    }
}